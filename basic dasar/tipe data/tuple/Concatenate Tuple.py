 # Concatenate Tuple

# Tuple 1
nama_ikan = 'Arwana', 'Arapaima', 'Lele', 'Duyung', 'Tawes', 'Mujaer'

# Tuple 2
nama_serangga = "Tawon", "Kupu-kupu", "Jentrung", "Nyamuk", "Jangkrik"

# Gabung Tuple 1 dan Tuple 2
nama_ikan_dan_serangga = nama_ikan + nama_serangga


# Cetak Tuple 1 & 2
print("Nama-nama ikan: ", nama_ikan)
print("Nama-nama serangga: ", nama_serangga)
print("\nGabungan nama-nama ikan dan serangga: ", nama_ikan_dan_serangga)
