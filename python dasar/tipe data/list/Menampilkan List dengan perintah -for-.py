# MENAMPILKAN LIST DENGAN 'FOR'

'''
Tantangan Akses List:
Buatlah sebuah program Python yang mengandung sebuah list dengan
nama angka yang berisi angka 1 hingga 5. Tampilkan elemen list
tersebut satu per satu dengan menggunakan loop for.
'''

angka = [1,2,3,4,5]
jumlah_list = len(angka)
for i in range(jumlah_list):
    print(angka[i])
    
