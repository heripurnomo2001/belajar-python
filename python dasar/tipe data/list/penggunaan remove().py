# PENGGUNAAN REMOVE()

'''
Remove digunakan untuk menghapus elemen pertama dari nilai
tertentu yang dikirim oleh function remove(x). 
'''

# Contoh

list1 = [1,2,3,4,3,5,2,8,2,10]

# Cetak sebelum diremove
print("Data list1 sebelum diremove: ", list1)

# Menghapus value = 2
list1.remove(2)

# Cetak setelah diremove
print("Data list1 setelah diremove 'list1.remove(2)' : ", list1)
print("\nAnda lihat bukan, elemen yang berisi nilai 2 yang pertama sudah tidak ada.")







