# Akses Karakter Positif dan Negatif
'''
Soal Pertama:
Buatlah sebuah string dengan nilai "Python Programming" dan lakukan hal berikut:
a. Cetak karakter pertama dalam string tersebut menggunakan indeks positif.
b. Cetak karakter terakhir dalam string tersebut menggunakan indeks negatif.
'''

# Menyiapakan string
string1 = "Python Progamming"
print(f"Akses Karakter String : '{string1}'\n")

# Cetak karakter pertama dengan indeks positif
print(f"Karakter pertama dari {string1} adalah {string1[0]}")

# Cetak karakter terakhir dengan indeks negatif
print(f"Karakter terakhir dari {string1} adalah {string1[-1]}")


