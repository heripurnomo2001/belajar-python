# Contoh Dictionary
dict = {"mobil": 1, "motor": 4, "kereta api": 1, "pesawat": 2, "kapal": 1}


# Buat Sort List by keys and values
mylist = list(dict.keys())
mylist.sort()
myvalue = list(dict.values())
myvalue.sort()

# Tampilkan Dict, List, dan Value
print("Dictionary : ", dict)
print("Sort List by keys :", mylist)
print("Sort List by myvalue :", myvalue)
